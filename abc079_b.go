package main
import "fmt"
func main() {
	var n int
	fmt.Scan(&n)
	l := make([]int, n+1)
	l[0] = 2
	l[1] = 1
	for i := 0; i < n-1; i++ {
		l[i+2] = l[i] + l[i+1]
	}
	fmt.Println(l[n])
}
